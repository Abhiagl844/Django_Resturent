function pop(){
    var click = document.getElementById("myDropdown");
    var clic = document.getElementById("myDropdown1");
    var cli = document.getElementById("acc-content");
    var cl = document.getElementById("ham"); 
    if(click.style.display === "none"){
        click.style.display = "block";
    if(clic.style.display === "block"){
        clic.style.display = "none";
    }else{
        clic.style.display = "none";
    }
    if(cli.style.display === "block"){
        cli.style.display = "none";
    }else{
        cli.style.display = "none";
    }
    if(cl.style.display === "block"){
        cl.style.display = "none";
    }else{
        cl.style.display = "none";
    }    
    }else{
        click.style.display = "none";
    }
}
